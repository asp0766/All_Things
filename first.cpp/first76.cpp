// POINTER
// pointer arithmetic       p++ and p--



#include <iostream>
using namespace std;
int main()
{
      int A[5]={2,4,6,8,10};
      int *p=A;
      cout<<*p<<endl;
      p++;                 // this is increase pointing value and also print the next value in array
      cout<<*p<<endl;
      p--;               //this command take previous or back forward in array
      cout<<*p<<endl;


      
     



      return 0;



    
}


 