// ARTHIMETIC OPERATOR 



// Define two numbers
let a = 10;
let b = 3;

// Addition
let sum = a + b;
console.log("Addition: " + sum); 

// Subtraction
let difference = a - b;
console.log("Subtraction: " + difference); 

// Multiplication
let product = a * b;
console.log("Multiplication: " + product); 

// Division
let quotient = a / b;
console.log("Division: " + quotient); 

// Modulus
let remainder = a % b;
console.log("Modulus: " + remainder); 

// Exponentiation
let power = a ** b;
console.log("Exponentiation: " + power); 