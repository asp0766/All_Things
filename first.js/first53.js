// Data and Time in Depth in JS : -
let myDate = new Date()
console.log(myDate.toString());           // Wed Feb 26 2025 22:55:46 GMT+0530 (India Standard Time)
console.log(myDate.toDateString());       // Wed Feb 26 2025
console.log(myDate.toLocaleDateString()); // 2/26/2025
console.log(typeof myDate);  // object

let myCreatedDate = new Date(2025 , 1 ,22 )
console.log(myCreatedDate.toString());       // Sat Feb 22 2025 00:00:00 GMT+0530 (India Standard Time)




    





