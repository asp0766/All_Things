// 2D Array : - 


public class first16 {
    public static void main(String[] args) {
        
     
        int[][] marks = {{10, 20} ,{30, 40},{50, 60} };
       

      
        for (int i = 0; i < marks.length; i++) {
            for (int j = 0; j < marks[i].length; j++) {
                System.out.println(marks[i][j]); 
            }
        }
    }
}
